                <div id="panel-element-11" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Las autoridades deberán crear una sección especial en el Registro de Condenas, a cargo del  Registro Civil accesible por vías informáticas, con las personas inhabilitadas para ejercer funciones en ámbitos educacionales o con menores de edad, según sentencia judicial ejecutoriada. </p>
