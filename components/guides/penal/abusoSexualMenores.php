            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-3">¿Qué otras conductas se consideran abusos contra menores? </a>
                </div>
                <div id="panel-element-3" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Estupro: Acceso carnal, por las vías ya mencionadas, a un menor de edad pero mayor de 14 años aprovechando un estado mental perturbado, abusando de alguna posición de autoridad (jefe, tutor o encargado de su cuidado), aprovechando el desamparo de la víctima o su inexperiencia o ignorancia sexual.</p>
                        <p>También se castiga una relación carnal homosexual con un menor de 18 años, aun cuando no se cumplan las condiciones que definen la violación o el estupro.</p>
                        <p>Actos sexuales con menores, que involucren introducir objetos por vía bucal, vaginal, anal o que involucren animales.</p>
                        <p>Pagar por servicios sexuales de menores de edad.</p>
                        <p>En general, cualquier acto sexual que afecte los genitales, ano o boca de un menor de edad.</p>
                        <p></p>
                        <p></p>
                        <p></p>
