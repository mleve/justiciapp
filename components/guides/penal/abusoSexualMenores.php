                        <p></p></div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-4">¿La ley castiga otras conductas que no necesariamente son actos sexuales con menores?</a>
                </div>
                <div id="panel-element-4" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Sí. La ley también califica como delitos: </p>
                        <p>Promover o facilitar la prostitución de menores.</p>
                        <p>Producir o distribuir material pornográfico en que participen menores de edad.</p>
                        <p>Exponer a menores a material pornográfico para lograr excitación.</p>
                        <p>Realizar acciones de significación sexual para procurar su excitación o la excitación de otro ante personas menores de edad. </p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p></div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-5">¿Cuáles son las penas para los que cometen estos delitos? </a>
                </div>
                <div id="panel-element-5" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Violación a un menor de catorce años: De 5 años y un día a 20 años.</p>
                        <p>Estupro: De 3 años y un día a 10 años.</p>
                        <p>Relación homosexual con un menor: de 61 días a 3 años.</p>
                        <p>Involucrar a menores en actos sexuales con animales o introduciéndoles objetos: Cuando hay circunstancias que se definan como violación (hacer uso de fuerza o cuando la víctima no puede resistirse), 5 años y un día a 15 años; de 5 años y un día a 20 años si la víctima es menor de 14 años, y de 3 años y un día a 10 años si la víctima tiene entre 14 y 18 años y se cumplen las condiciones para estupro.</p>
                        <p>Pagar por servicios sexuales de un menor: 3 años y un día a 5 años.</p>
                        <p>Cualquier otro acto sexual distinto de la violación, realizado de manera abusiva con menores de edad: 3 años y un día a 5 años si la víctima tiene entre 14 y 18 años, y de 3 años y un día a 10 años si la víctima es menor de 14 años.</p>
                        <p>Exponer a menores a pornografía para excitación: De 541 días a 5 años.</p>
                        <p>Hacer que menores realicen acciones sexuales para excitar a otro: 3 años y un día a 5 años. </p>
                        <p>Producir material pornográfico: 3 años y un día a 5 años.</p>
                        <p>Comercializar, importar, distribuir, difundir o exhibir  material pornográfico, cualquiera sea su soporte, en cuya elaboración hayan sido utilizados menores de dieciocho años: de 3 años y un día a cinco años.</p>
                        <p>Facilitar prostitución de menores: 3 años y un día a 5 años.</p>
