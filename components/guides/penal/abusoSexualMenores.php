                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-12">¿Los condenados por delitos sexuales contra menores pueden obtener la libertad condicional?</a>
                </div>
                <div id="panel-element-12" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> La mayoría de los condenados por diversos delitos pueden aspirar a la libertad condicional al cumplir la mitad de la pena. Pero quienes están sentenciados por algunos delitos graves, que define la legislación, sólo pueden postular al cumplir dos tercios de la condena. Entre otros delitos, es el caso de los condenados por violación de una persona menor de 14 años, los sentenciados por cometer acciones sexuales con menores de 14 años, por producir pornografía con menores de 18 años, por facilitar la prostitución infantil o por participar en  la explotación sexual infantil, el trabajo forzado infantil, la servidumbre y la esclavitud de niños, niñas y adolescentes y los que facilitan que se cometan esos delitos.</p>
