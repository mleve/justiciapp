                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-7">¿Quién puede denunciar abusos contra menores?</a>
                </div>
                <div id="panel-element-7" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Aunque lo ideal es que la propia víctima o su representante legal (padre, tutor, etc.) denuncie ante la Justicia, cualquier persona puede hacerlo si sabe de un caso de esta naturaleza. Eso, independiente de que la Policía inicie sus propias investigaciones, en especial cuando afectan a menores de edad.</p>
