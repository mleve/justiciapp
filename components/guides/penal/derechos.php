        <h4>Derechos de las víctimas</h4>
        <p>En el proceso penal se considera víctimas a los afectados directamente por un delito. En delitos cuya consecuencia sea la muerte de la persona y en los casos en que el afectado no puede ejercer sus derechos, por algún impedimento grave, se considerará víctima al cónyuge y los hijos. Si no hay cónyuges o hijos, se considerará víctimas a los ascendientes (por ejemplo, los padres o abuelos). Si no hay ascendientes, al conviviente y si ni hay convivientes, a los hermanos.</p>
        <table class="table table-hover">
            <thead>
            <th>Derecho</th>
            <th>Explicación</th>
            </thead>
            <tbody>
                <tr>
                    <td>Atención</td>
                    <td>Como víctima, tiene derecho a ser atendida por los fiscales, carabineros, policías de la PDI y los jueces.</td>
                </tr>
                <tr>
                    <td>Denuncia</td>
                    <td>Si es víctima, tiene derecho a denunciar el delito ante Carabineros, la PDI o la Fiscalía (Ministerio Público).</td>
                </tr>
                <tr>
                    <td>Información</td>
                    <td>Tiene derecho a ser informada acerca del estado de la causa, de sus derechos y de lo que deba hacer para ejercerlos.</td>
                </tr>
                <tr>
                    <td>Participación</td>
                    <td>Como víctima, tiene derecho a que Carabineros, PDI, Fiscalía, Registro Civil, Gendarmería y Servicio Médico Legal le den apoyo y facilidades para realizar los trámites en que deba intervenir.</td>
                </tr>
                <tr>
                    <td>Solicitar protección </td>
                    <td>La víctima tiene derecho a solicitar que la Fiscalía vele por su protección en todas las etapas del procedimiento penal. Hay medidas de protección que puede adoptar la Fiscalía y otras medidas superiores que toman los jueces. Las víctimas pueden solicitar medidas de protección frente a hostigamientos, amenazas o atentados en contra de ellas o de su familia.</td>
                </tr>
                <tr>
                    <td>Querella</td>
                    <td>La víctima tiene derecho a querellarse, esto es a demandar, contra uno o más imputados identificándolos o contra quienes resulten responsables del delito sin dar nombres.</td>
                </tr>
                <tr>
                    <td>Demandar al imputado</td>
                    <td>La víctima tiene derecho a presentar acciones legales contra el imputado por las responsabilidades civiles, además de las penales, que deriven del hecho delictual. Las demandas civiles pueden dar lugar al pago de indemnizaciones si así lo determina el tribunal.</td>
                </tr>
                <tr>
                    <td>Audiencia</td>
                    <td>La víctima tiene derecho a ser oída por el fiscal o el tribunal antes de que se pida o se decida la suspensión del juicio, la terminación anticipada, el sobreseimiento, la decisión de no perseverar, o cualquier resolución que ponga término a la causa.</td>
                </tr>
                <tr>
                    <td>Recurrir a una sentencia</td>
                    <td>Tienen derecho a impugnar el sobreseimiento o la sentencia absolutoria, aun cuando la víctima no haya intervenido en el procedimiento.</td>
                </tr>
                <tr>
                    <td>Buen trato</td>
                    <td>Tiene derecho a recibir un trato digno y acorde con su condición de víctima.</td>
                </tr>
                <tr>
                    <td>Reclamo</td>
                    <td>Tiene derecho a reclamar ante la Fiscalía o ante el juez, ante las decisiones que lo afecten.</td>
                </tr>
            </tbody>
        </table>
        <h4>Derechos de los Imputados</h4>
        <p>En el proceso penal se consideran imputados a aquellas personas a las que se le atribuye participación en un hecho punible, es decir sancionable penalmente.</p>
        <table class="table table-hover">
            <thead>
                <th>Derecho</th>
                <th>Explicación</th>
            </thead>
            <tbody>
                <tr>
                    <td>Derecho a información</td>
                    <td>Tiene derecho a que se le informe de manera específica y clara los hechos que se le imputan y qué derechos le otorgan la Constitución y las leyes. </td>
                </tr>
                <tr>
                    <td>Presunción de inocencia</td>
                    <td>Ninguna persona será considerada culpable ni tratada como tal hasta que sea debidamente condenada por el tribunal.</td>
                </tr>
                <tr>
                    <td>Derecho a pedir diligencias</td>
                    <td>Durante la etapa de investigación, que desarrolla la Fiscalía, tiene derecho a pedir diligencias para aclarar los hechos o para que se desvirtúen las imputaciones en su contra.</td>
                </tr>
                <tr>
                    <td>Trato digno</td>
                    <td>No puede ser sometido a tortura ni a tratos crueles, inhumanos o degradantes.</td>
                </tr>
                <tr>
                    <td>Derecho a declarar</td>
                    <td>Tiene derecho a guardar silencio. Pero si accede a prestar declaración tiene derecho a no hacerlo bajo promesa o juramento. </td>
                </tr>
                <tr>
                    <td>Juicio único</td>
                    <td>Quien haya sido condenado, absuelto o sobreseído definitivamente, es decir cuando el fallo no tenga recursos pendientes, no podrá ser sometido a un nuevo procedimiento penal por el mismo hecho. </td>
                </tr>
                <tr>
                    <td>Derecho a defensa</td>
                    <td>El imputado tiene derecho a ser defendido por un abogado particular o provisto por el Estado desde el inicio de la causa en su contra. La Defensoría Penal pública es un organismo del Estado, sometido a la supervigilancia del Presidente de la República, que debe poner abogado a los imputados que no desean tener abogado particular o no tengan los recursos para contratar uno. También tiene derecho a defenderse solo, siempre que el Tribunal lo autorice.</td>
                </tr>
                <tr>
                    <td>Recurso de amparo</td>
                    <td>El detenido tiene derecho a interponer un recurso de amparo si considera que su detención o prisión se hizo contra la ley o la Constitución.</td>
                </tr>
                <tr>
                    <td>Derecho a juicio</td>
                    <td>Toda persona tiene derecho a un juicio previo, oral y público. Nadie podrá ser condenado si no es por sentencia dictada por un tribunal imparcial, conforme a una ley que exista antes de que se cometa el hecho investigado.</td>
                </tr>
                <tr>
                    <td>Juicio presencial</td>
                    <td>El imputado no puede ser juzgado en ausencia.</td>
                </tr>
                <tr>
                    <td>Sobreseimiento de la causa</td>
                    <td>Puede solicitar el sobreseimiento definitivo de la causa y apelar en caso de rechazo.</td>
                </tr>
            </tbody>            
        </table>
        <h4>Derechos de los imputados privados de libertad</h4>
        <p>Además de los derechos de todos los imputados, el estar privado de libertad otorga otros derechos:</p>
        <table class="table table-hover">
            <thead>
            <th>Derecho</th>
            <th>Explicación</th>
            </thead>
            <tbody>
                <tr>
                    <td>Derecho a información</td>
                    <td>Tiene derecho a que se le exprese específica y claramente el motivo de su privación de libertad.</td>
                </tr>
                <tr>
                    <td>Detención policial por un máximo de 24 horas</td>
                    <td>Tiene derecho a que su detención en un recinto policial no dure más de 24 horas.</td>
                </tr>
                <tr>
                    <td>Presentarse ante el tribunal</td>
                    <td>Tiene derecho a ser conducido sin demora ante el tribunal que ordenó su detención.</td>
                </tr>
                <tr>
                    <td>Solicitud de libertad</td>
                    <td>Tiene derecho a solicitar al tribunal que le conceda la libertad, independiente del resultado de esta solicitud.</td>
                </tr>
                <tr>
                    <td>Información a familiares</td>
                    <td>Tiene derecho a que el encargado de guardia del recinto policial en que se encuentre, informe a un familiar, en su presencia, que está detenido o preso, los motivos, y el lugar donde se encuentra.</td>
                </tr>
                <tr>
                    <td>Derecho a hablar con su abogado</td>
                    <td>Tiene derecho a entrevistarse privadamente con su abogado de acuerdo al régimen del establecimiento de detención.</td>
                </tr>
                <tr>
                    <td>Comodidades</td>
                    <td>Tiene derecho a contar con comodidades y ocupaciones compatibles con la seguridad del recinto en que se encuentre, que deben ser a expensas del imputado.</td>
                </tr>
                <tr>
                    <td>Derecho a visitas</td>
                    <td>Tiene derecho a recibir visitas y comunicarse por escrito o por cualquier otro medio, a menos que la comunicación interfiera con la investigación policial.</td>
                </tr> 
            </tbody>            
        </table>
        <h4>Derechos de  los niños, niñas y adolescentes imputados</h4>
        <table class="table table-hover">
            <thead>
            <th>Derecho</th>
            <th>Explicación</th>
            </thead>
            <tbody>
                <tr>
                    <td>Prisión en centros especiales</td>
                    <td>Aunque todos los niños, niñas y adolescentes de entre 14 y 18 años son responsables ante la ley penal, tienen derecho a defensa gratuita, y de ser condenados a encierro, no son derivadas a recintos carcelarios adultos, sino a centros especiales.</td>
                </tr>
                <tr>
                    <td>Programas de rehabilitación</td>
                    <td>Los niños, niñas y adolescentes imputados tienen un conjunto de garantías, como acceso a educación y programas de rehabilitación antidrogas y alcohol.</td>
                </tr>
                <tr>
                    <td>Derechos de todo imputado</td>
                    <td>Los presuntos infractores de ley que tengan entre 14 y 18 años tiene los mismos derechos que los adultos imputados, como el derecho a guardar silencio, el derecho a la información, a ser llevado ante un juez y a tener un abogado.</td>
                </tr>
                <tr>
                    <td>Derecho al sistema especial de penas</td>
                    <td>La Ley de Responsabilidad Penal Adolescente contempla un estatuto de penas diferente a la ley común, que incluye como sanción máxima la internación en régimen cerrado por diez años en los casos más graves. Existe también la internación en régimen semi cerrado, libertad asistida, libertad asistida especial, trabajos comunitarios, multas y amonestaciones.</td>
                </tr>
                <tr>
                    <td>Pedir cambio de pena</td>
                    <td>Una vez sancionado, el joven tiene derecho a pedir el término o cambio de una pena privativa de libertad, por una que pueda cumplir en libertad para favorecer su reinserción social.</td>
                </tr>
            </tbody>            
        </table>               
