            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-2">¿Qué establece la Ley de Responsabilidad Penal Adolescente?</a>
                </div>
                <div id="panel-element-2" class="panel-collapse collapse in">
                    <div class="panel-body">
                        <p> La  ley de Responsabilidad Penal Adolescente  establece un sistema de responsabilidad para los adolescentes entre 14 y 18 años que violen la ley penal. Su principal objetivo es reinsertar a los jóvenes en la sociedad a través de programas especiales.</p>
                        
                        
                        
                        
                        
                        </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-3">¿Cuáles son sus principales características? </a>
                </div>
                <div id="panel-element-3" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Establece procedimientos, fiscales y defensores especializados.</p>
                        <p>Establece programas de reinserción.</p>
                        <p>Termina con el trámite de discernimiento.</p>
                        <p>Establece la responsabilidad penal desde los 14 años, distinguiendo dos segmentos: 14 a 16 y de 16 a 18 años.</p>
                        <p>Establece un amplio catálogo de sanciones.</p>
                        <p>Las penas privativas de libertad sólo se establecen para delitos más graves.</p>
                        </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-4">¿Cuál es la diferencia con la ley anterior?</a>
                </div>
                <div id="panel-element-4" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> En la normativa anterior los menores entre 14 y 16 años no eran imputables penalmente, es decir, no se les podía aplicar una pena a pesar de haber cometido un ílicito o delito. Sólo se les aplicaba medidas de protección a cargo del Servicio Nacional de Menores.</p>
                        <p>En el caso de los adolescentes de entre 16 y 18 años, se les aplicaba un examen de discernimiento, para verificar si estaban conscientes del delito cometido. Si el juez determinaba que había conciencia de ello, el menor era condenado como un adulto y recluido en recintos de Gendarmería. De lo contrario, pasaba a los centros del Sename, bajo la figura de protección, sin derecho a defensa gratuita, sin límite de tiempo y sin las garantías de un debido proceso.</p>
                        <p> Hoy todos los adolescentes entre 14 y 18 años son responsables ante la ley penal, tienen derecho a defensa gratuita, y de ser condenados a encierro, no son derivadas a recintos carcelarios adultos, sino a centros especiales. Además, reciben un conjunto de garantías, como acceso a educación y programas de rehabilitación antidrogas y alcohol.</p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-6">¿Qué derechos tienen los jóvenes infractores? </a>
                </div>
                <div id="panel-element-6" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Conocer el motivo de su detención y ver la orden de la misma, salvo que sea sorprendido de modo flagrante.</p>
                        <p>Ser adecuadamente informado de los hechos que se le imputan, así como de los derechos que le otorga la ley: guardar silencio, no ser obligado a declarar y ser trasladado ante un juez dentro del plazo de 24 horas.</p>
                        <p>Contar con un abogado de su confianza. Si no lo tuviese, el Estado deberá proporcionarle uno.</p>
                        <p>Mientras se determina su culpabilidad, tiene derecho a ser tratado como inocente.</p>
                        <p>Si el inculpado se encuentra privado de libertad, tiene derecho a permanecer separado de los adultos.</p>
                        <p>Debe ser tratado con dignidad, no pudiendo ser sometido a castigos corporales, encierro en celda oscura o a cualquier sanción que ponga en riesgo su salud física y mental.</p>
                        <p>Una vez sancionado, el joven tiene derecho a pedir el término o cambio de una pena privativa de libertad, para que pueda cumplirla en libertad para favorecer su reinserción social.</p>
                    </div>
                </div>
            </div>

            <div class="panel panel-default">
