                </div>
                <div id="panel-element-8" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Sanciones de tres tipos: privativas de libertad, no privativas de libertad y sanciones accesorias.</p>
                        
                        
                        
                        
                        
