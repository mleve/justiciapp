            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-2">¿En qué consiste la Reforma Procesal Penal?</a>
                </div>
                <div id="panel-element-2" class="panel-collapse collapse in">
                    <div class="panel-body">
                        <p> Es un cambio en el sistema de procesamiento de las causas judiciales penales, que terminó con el sistema inquisitivo, y estableció un esquema acusatorio, en que se dividen las funciones de investigar, procesar, y dictar sentencia en las causas.</p>



                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-3">¿Cuáles son las principales diferencias entre el antiguo sistema y el nuevo?</a>
                </div>
                <div id="panel-element-3" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Se separan las funciones de investigar, procesar (en el antiguo sistema), formalizar y acusar (en el nuevo sistema) y de dictar sentencia.</p>
                        <p>Los juicios son orales y públicos y no escritos como eran antes.</p>
                        <p>Se crea el Ministerio Público, cuyos fiscales son los encargados de dirigir las investigaciones, formalizar y acusar a los imputados.</p>
                        <p>Se crea la Defensoría Penal Pública para otorgar defensa profesional a los que no pueden tener a abogado.</p>
                        <p>Los casos ya no están en manos de juzgados del crimen, sino que se crean los tribunales de garantía y los tribunales de juicio oral en lo penal.</p></div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-4">¿Qué es el Ministerio Público?</a>
                </div>
                <div id="panel-element-4" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> El ministerio publico (Fiscalía) es un organismo estatal autónomo, que no es parte de ninguno de los poderes del Estado, y que se encarga de dirigir la investigación de los delitos, formalizar la investigación en contra de determinadas personas, acusar a los presuntos responsables y proteger a víctimas y testigos. Trabaja en coordinación con Carabineros e Investigaciones y los organismos auxiliares, como por ejemplo, el Servicio Médico Legal y el Servicio de Registro Civil e Identificación.</p>



                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-5">¿Qué es un fiscal?</a>
                </div>
                <div id="panel-element-5" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Es el representante del Ministerio Público, que se encarga de investigar los delitos y si el caso lo amerita, llevar ante los tribunales a los imputados como autores, cómplices o encubridores. Durante la investigación, el fiscal debe indagar lo que perjudique al imputado, pero el principio de objetividad lo obliga también a averiguar aquello que lo favorezca. En los juicios, defenderá los intereses del Estado y presentará evidencias en contra del acusado. </p>



                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-6">¿Qué es la Defensoría Penal Pública?</a>
                </div>
                <div id="panel-element-6" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Es la entidad estatal encargada de proveer un abogado defensor a todas las personas que así lo requieran y no contraten defensa particular. Funciona bajo la supervigilancia del Presidente de la República, a través del Ministerio de Justicia. La labor de esta institución es dar defensa legal y profesional al imputado, informarle de sus derechos y de los procedimientos legales en que se verá involucrado, y solicitar al tribunal, medidas de protección en su favor. Estos defensores son públicos o pueden ser privados, licitados por la Defensoría.</p>



                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-7">¿Tiene costo solicitar los servicios de la Defensoría Penal Pública?</a>
                </div>
                <div id="panel-element-7" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Si se cuenta con los recursos suficientes, el servicio debe ser pagado. Si la persona no tiene los medios económicos, el abogado trabajará en forma gratuita para el imputado.</p>



                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-8">¿Cómo consigo un abogado defensor?</a>
                </div>
                <div id="panel-element-8" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Si le imputan un delito, le preguntarán si tiene un abogado particular que se encargue de su defensa. Si no tiene, le asignarán un profesional de la Defensoría Penal Pública.</p>



                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-9">¿Qué hace un juez de garantía?</a>
                </div>
                <div id="panel-element-9" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Un juez de garantía debe, principalmente, velar porque las autoridades (en particular los fiscales y los policías) no se extralimiten al perseguir a los sospechosos de un delito, afectando los derechos fundamentales del imputado. Debe decidir sobre cualquier materia que le consulten los fiscales durante sus investigaciones, en especial si es sobre alguna diligencia que perturbe los derechos garantizados en la Constitución, como por ejemplo, la privación de libertad o un allanamiento. Al juez de garantía también le corresponde aprobar las soluciones alternativas y los juicios abreviados o simplificados.</p>



                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-10">¿Qué es el archivo provisional?</a>
                </div>
                <div id="panel-element-10" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Cuando los fiscales no encuentran antecedentes suficientes para seguir adelante una investigación, pueden archivar provisionalmente una causa sin acusados. Las víctimas pueden pedir que un caso se reactive si aparece nueva información.</p>



                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-11">¿Existen salidas alternativas durante el desarrollo de un juicio?</a>
                </div>
                <div id="panel-element-11" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Sí. Puede darse la posibilidad de que las partes lleguen a un acuerdo reparatorio o que el juez dicte la suspensión condicional del procedimiento, bajo ciertas exigencias o condiciones impuestas al acusado. También existen juicios simplificados y juicios abreviados, que se aplican en casos de delitos con menor penalidad. Esos procedimientos los lleva el fiscal ante un juez de garantía.</p>



                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-12">¿Qué son los juicios orales?</a>
                </div>
                <div id="panel-element-12" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> El sistema está diseñado para que sólo los casos de mayor gravedad lleguen al juicio oral, en el que en una o más audiencias el fiscal y el defensor presentan pruebas y testigos ante los tres jueces del Tribunal Oral en lo Penal. Luego de concluir la presentación de pruebas y los alegatos de las partes, los jueces dictan sentencia, que puede ser una condena o la absolución del o los imputados.</p>



                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-13">¿Qué pasa con los casos que se iniciaron en el sistema antiguo?</a>
                </div>
                <div id="panel-element-13" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Deberán seguir de acuerdo a los procedimientos anteriores a la reforma. Para ello quedan algunos juzgados operando en el régimen anterior.  </p>



