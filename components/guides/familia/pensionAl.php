        <div class="panel-group" id="panel-questions">

            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-2">¿Qué son las pensiones alimenticias?
                    </a>
                </div>
                <div id="panel-element-2" class="panel-collapse collapse in">
                    <div class="panel-body">
                        <p>Es la obligación de dar alimentos. Tratándose de niños, niñas y adolescentes, incluye además, enseñanza básica, media y el aprendizaje de alguna profesión u oficio.</p>








                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-3">¿Quiénes son beneficiarios de las pensiones alimenticias?
                    </a>
                </div>
                <div id="panel-element-3" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Los hijos(as) hasta que cumplan 21 años salvo que estén estudiando una profesión u oficio, caso en el cual cesarán a los 28 años o que les afecte una incapacidad física o mental que los inhabilite para subsistir por si mismos.</p>








                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-4">¿Quiénes deben proporcionar la pensión alimenticia a los hijos?
                    </a>
                </div>
                <div id="panel-element-4" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Ambos padres en proporción a sus capacidades económicas.</p>








                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-5">¿Cómo tramito la pensión alimenticia?
                    </a>
                </div>
                <div id="panel-element-5" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>En caso de separación, el cuidado de personal de los hijos corresponde al padre y a la madre. Ambos, aunque vivan separados, participarán en forma activa, equitativa y permanente en la crianza y educación y mantención de sus hijos. El cuidado personal puede recaer en uno de los padres por acuerdo o resolución judicial, pero el otro siempre debe aportar a la mantención. Si no lo hace, se puede demandar ante la justicia el pago de la pensión alimenticia. Las adolescentes embarazadas también tienen derecho a demandar pensión de alimentos por el hijo(a) que está por nacer, sin necesidad de contar con un representante legal. Para ello hay dos caminos: </p>
                        <p>Extrajudicialmente: Puede intentar un acuerdo con la persona obligada a pagar alimentos para fijar la pensión de alimentos. Debe quedar por escrito, firmado por ambos y autorizado por un notario o el jefe de la Corporación de Asistencia Judicial. También se puede recurrir a un mediador.
                            Luego, este documento debe presentarse ante el juzgado de familia para que sea aprobado y tenga la misma fuerza que una sentencia judicial. Así, en caso de que la persona obligada no cumpla, podrá exigir el cumplimiento forzado de ese acuerdo, mediante el despacho de una orden de arresto u otro apremio. </p>
                        <p>Judicialmente: Si la persona obligada no da voluntariamente la pensión de alimentos a sus hijos(as), o no es posible lograr un acuerdo extrajudicial, debe interponerse una demanda de pensión de alimentos ante el juzgado de familia competente, esto es el del domicilio del niño(a), el que derivará a las partes a Mediación, por tratarse de una de las materias en que la ley exige someterse a dicho proceso. </p>






                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-6">¿Se requiere contar con un abogado para presentar la demanda de alimentos?
                    </a>
                </div>
                <div id="panel-element-6" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Sí, se necesita siempre un abogado para demandar, salvo que el juez haga una excepción por motivos fundados.</p>








                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-7">¿Qué se debe probar en el juicio de alimentos? </a>
                </div>
                <div id="panel-element-7" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>El vínculo de parentesco con el demandado: mediante certificado de nacimiento o libreta de matrimonio.</p>
                        <p>Las necesidades del niño/a: a través de una lista con sus respectivos comprobantes de los gastos de alimentación, educación, recreación, vivienda, salud, vestuario, movilización, luz, agua, gas, teléfono, etc.</p>
                        <p>La capacidad económica y patrimonial del demandado: mediante liquidaciones de sueldo, declaración de impuesto a la renta, boletas de honorarios y antecedentes de su patrimonio o declaración jurada. Si se ocultan las fuentes de ingreso o se presentan antecedentes falsos, se arriesga a sanciones con penas de prisió</p>






                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-8">Mientras se desarrolla el juicio, ¿el demandante queda sin pensión alimenticia?</a>
                </div>
                <div id="panel-element-8" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> No, en la primera actuación judicial de un juicio de derecho de alimentos, el juez tiene la obligación de fijar el monto de dinero que el demandado deberá pagar para los hijos menores de edad mientras se tramita el juicio de alimentos y hasta que se dicte sentencia definitiva. Esto se conoce como alimentos provisorios.</p>








                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-9">¿Cómo se recibe la pensión de alimentos decretada por el juez?</a>
                </div>
                <div id="panel-element-9" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>También es posible que se imputen a la pensión alimenticia ciertos pagos efectuados en especies, situación que el tribunal regulará en la sentencia. Por ejemplo: el pago de la o las colegiaturas. El tribunal puede disponer además de otros canales de pago.</p>








                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-10">¿Cuáles son los montos establecidos para solicitar la pensión de alimentos? </a>
                </div>
                <div id="panel-element-10" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>El monto mínimo equivale al 40% de un ingreso mínimo cuando se trate de un solo hijo. Si tiene más de un hijo, el monto mínimo por cada uno de ellos equivale al 30% de un ingreso mínimo. </p>
                        <p>El monto máximo no podrá sobrepasar el 50% de los ingresos totales de quien pagará la pensión. Ejemplo: si el padre o madre que da la pensión tiene ingresos totales equivalentes a 600.000 pesos, y se solicita la pensión para 3 hijos, la pensión total (por los 3 hijos) no podra ser superior a 300.000 pesos, a pesar de que las necesidades de los menores sean mayores a esa cifra.</p>







                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-11">¿Qué pasa si el demandado no paga la pensión de alimentos?</a>
                </div>
                <div id="panel-element-11" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> El juez puede: </p>
                        <ul>
                            <li>Suspender su licencia de conducir hasta por seis meses.</li>
                            <li>Retener su devolución a la renta.</li>
                            <li>Castigar a quien colabore en el ocultamiento del demandado con el fin de impedir su notificación o el cumplimiento de sus obligaciones parentales, con la pena de reclusión nocturna hasta por 15 días.</li>
                            <li>Ordenar arresto nocturno (22:00-06:00 hrs.) hasta por 15 días. Si cumplido el arresto, el demandado deja de pagar la pensión correspondiente al mes siguiente, el juez puede repetir esta medida hasta obtener el pago total de la pensión de alimentos adeudada.</li>
                            <li>Ordenar arresto completo hasta por 15 días, si no cumple el arresto nocturno decretado o no paga la pensión de alimentos después de dos períodos de arresto nocturno. En caso que se den nuevos incumplimientos, el juez podrá ampliar el arresto hasta por 30 días. Tanto en el caso del arresto nocturno como en el arresto completo,
                                si el demandado no es encontrado en el domicilio que se señala en el expediente, el juez deberá adoptar todas las medidas necesarias para que el arresto se cumpla.</li>
                            <li>Oficiar al empleador del demandado (si es trabajador dependiente) a que deposite el dinero correspondiente a la pensión y se haga efectiva la multa que la ley establece como sanción para el empleador.</li>
                            <li>Ordenar su arraigo o prohibición para salir fuera del país hasta que se efectúe el pago de lo adeudado. También se puede solicitar el arraigo al tribunal cuando existan motivos fundados para estimar que se ausentará del país y no dejará garantía para el pago de la pensión regulada o aprobada por el tribunal.</li>
                            <li>Solicitar que se constituyan garantías sobre los bienes de su propiedad, de manera de asegurar el pago de la pensión. Por ejemplo: si el demandado es dueño de una casa, se puede solicitar que la renta de arriendo de esa casa se impute a alimentos, o que se le prohíba venderla para asegurar el pago de las pensiones futuras.</li>
                            <li>Embargar y rematar los bienes del demandado, hasta el pago total de la pensión. Lo más frecuente es que se fije una suma de dinero a pagar mensualmente, mediante depósito en una cuenta bancaria especial a nombre del demandante. Si el demandado es trabajador dependiente con empleo fijo, el juez oficiará al empleador para que descuente la pensión de alimentos directamente de su sueldo y la deposite en una cuenta del Banco Estado determinada por el juez.</li>
                        </ul>
