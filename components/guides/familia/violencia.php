            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-2">¿Qué es la violencia intrafamiliar?</a>
                </div>
                <div id="panel-element-2" class="panel-collapse collapse in">
                    <div class="panel-body">
                        <p> La violencia intrafamiliar se define como todo maltrato que afecte la vida o la integridad física o síquica de quien tenga o haya tenido la calidad de cónyuge o conviviente del ofensor o una relación de convivencia con él; o sea, pariente por consanguinidad (por ejemplo padres e hijos) o por afinidad en toda la línea recta o en la colateral hasta el tercer grado inclusive del ofensor (por ejemplo tios y sobrinos), su cónyuge o su actual conviviente, o bien, cuando esta conducta ocurre entre los padres de un hijo común, o sobre un menor de edad, adulto mayor o discapacitado que se encuentre bajo el cuidado o dependencia de cualquiera de los integrantes del grupo familiar.</p>

                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-4">¿Qué garantías ofrece esta ley?
                    </a>
                </div>
                <div id="panel-element-4" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Crea el delito de maltrato habitual que castiga con cárcel la violencia psíquica y física ejercida habitualmente; aumenta las sanciones por este tipo de conductas; modifica el Código Penal, aumentando en un grado las penas de cárcel en caso de lesiones causadas por violencia intrafamiliar; otorga mayores garantías de obtener protección para quienes denuncien maltratos, así como la obligación del agresor de abandonar la casa; la prohibición de que se acerque a la víctima, a su casa o a su lugar de trabajo, así como, a cualquier otro lugar al que ésta concurra o visite habitualmente; la obligación de asistir a terapia; y la facultad concedida a las policías para incautarle toda clase de armas, aunque tenga autorización para tenerlas.</p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-5">¿Qué organismos judiciales se ocupan de la violencia intrafamiliar? </a>
                </div>
                <div id="panel-element-5" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Los tribunale de familia, cuando los actos de violencia intrafamiliar no constituyan delito.</p>
                        <p>El ministerio publico, cuando los actos de violencia intrafamiliar constituyan un delito, sea que le remitan los antecedentes las policias o por denuncia directa de la persona afectada o un tercero. Ademas, este ministerio dará curso a la investigación pertinente en caso de que se presente el delito de maltrato habitual, si el respectivo Juzgado de Familia le ha remitido los antecedentes.</p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-6">¿Existe algún registro de las personas que hayan sido condenadas por violencia intrafamiliar?</a>
                </div>
                <div id="panel-element-6" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Sí. El registro civil tiene la obligación de llevar un registro especial de las personas condenadas como autoras de violencia intrafamiliar, así como de las demás resoluciones que la ley ordene transcribir. Dicha informacion es contenida mediante una glosa o indicacion especial en el certificado de antecedentes.</p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-7">¿Quiénes son sujetos protegidos por la ley de  violencia intrafamiliar?</a>
                </div>
                <div id="panel-element-7" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> El cónyuge, ex cónyuges, conviviente, ex conviviente, padre o madre de hijo común, aunque no haya existido convivencia. Se incluyen por igual a los parientes del ofensor, de su cónyuge o actual conviviente, en su línea recta (toda la ascendencia y descendencia) o  colaterales hasta la relación tíos / tías/ sobrinos / sobrinas, y además, cualquier otra persona que sea menor de edad, adulto mayor o con discapacidad que se encuentre bajo la dependencia de cualquier integrante de la familia.</p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-8">¿Cuáles son las sanciones para quienes cometan violencia intrafamiliar?
                    </a>
                </div>
                <div id="panel-element-8" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Se castigará con una multa de media a 15 UTM (Unidades Tributarias Mensuales) a beneficio del Gobierno Regional del domicilio del denunciante o demandante, para ser destinada a los centros de atención de víctimas de violencia intrafamiliar existentes en la región respectiva y que sean de financiamiento público o privado. Además, el juez deberá aplicar en la sentencia una o más de las siguientes medidas:</p>
                        <p>a) Obligación de abandonar el hogar que comparte con la víctima.</p>
                        <p>b) Prohibición de acercarse a la víctima o a su domicilio, lugar de trabajo o de estudio, así como, a cualquier otro lugar al que ésta concurra o visite habitualmente. Si ambos trabajan o estudian en el mismo lugar se oficiará al empleador o director del establecimiento para que adopte las medidas de resguardo necesarias.</p>
                        <p>c) Prohibición de porte y tenencia o el comiso de armas de fuego. La infracción de algunas de las tres medidas anteriores, puede acarrear sanción penal o arresto hasta por 15 días para el ofensor.</p>
                        <p>d) La asistencia obligatoria a programas terapéuticos o de orientación familiar.</p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-9">¿Qué es el delito de maltrato habitual?</a>
                </div>
                <div id="panel-element-9" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> El ejercicio habitual de violencia física o síquica respecto de las personas protegidas por la ley de violencia intrafamiliar. El requisito determinante de este delito es que los actos de violencia intrafamiliar sean habituales.</p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-10">¿Cuál es la sanción para el delito de maltrato habitual? </a>
                </div>
                <div id="panel-element-10" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> La sanción varía entre los 61 a 540 días de presidio, salvo que el hecho constituya un delito de mayor gravedad, caso en que se aplica la pena asignada a éste último. </p>



                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-11">¿Existen medidas de protección para las víctimas? </a>
                </div>
                <div id="panel-element-11" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Sí, los tribunales de justicia penal pueden adoptar medidas cautelares para proteger a  la víctima, o bien, alguna de las medidas accesorias antes mencionadas, fijar su plazo de duración y prorrogarlo en caso de ser necesario.  Por su parte, el tribunal de familia que conozca de un juicio por violencia intrafamiliar, debe adoptar de oficio todas las medidas necesarias para llevarlo a su término, así como, para dar protección a las víctimas de violencia intrafamiliar.  </p>



                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-12">¿A qué lugar se debe ir en caso de ser víctima de este tipo de violencia?
                    </a>
                </div>
                <div id="panel-element-12" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Al juazgado de familia, a Carabineros de Chile o a la Policía de Investigaciones, que tienen la obligación de acoger la denuncia y orientar a la víctima en la posterior tramitación legal.  </p>



                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-13">Si quiero denunciar un caso de violencia intrafamiliar ¿también debo recurrir a Carabineros e Investigaciones?</a>
                </div>
                <div id="panel-element-13" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Sí, ellos remitirán la denuncia al juazgado de familia, pero además se puede recurrir directamente a éste. Luego, a través de la interposición de la denuncia o la demanda, se da inicio al juicio por violencia intrafamiliar.  </p>



