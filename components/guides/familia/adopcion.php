						 <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-5">¿Qué ocurre si, antes de terminar el trámite de adopción, uno de los cónyuges muere, se separan o divorcian?</a>
					</div>
					<div id="panel-element-5" class="panel-collapse collapse">
						<div class="panel-body">
							<p> Si en vida el cónyuge fallecido hubiere iniciado los trámites de adopción o aquel hubiere manifestado su voluntad de adoptar, la adopción se entenderá efectuada por ambos cónyuges. Similar situación ocurre respecto de los cónyuges que se separan judicialmente o se divorcian, después de iniciada la tramitación de una adopción.</p>
