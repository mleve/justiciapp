					<div class="panel-heading">
						 <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-3">¿Quiénes pueden adoptar? </a>
					</div>
					<div id="panel-element-3" class="panel-collapse collapse">
						<div class="panel-body">
							<p>En primer lugar, pueden adoptar los matrimonios chilenos o extranjeros con residencia permanente en Chile.</p>
							<p>A falta de ellos, los cónyuges chilenos o extranjeros no residentes en Chile.</p>
							<p>A falta de matrimonios residentes en Chile o en el extranjero, pueden postular las personas solteras, divorciadas o viudas, chilenas o extranjeras, con residencia permanente en el país. Ahora, para elcaso en que hubiere varios interesados solteros o viudos que reúnan similares condiciones, el tribunal de familia preferirá a quien sea pariente consanguíneo del menor de edad, y en su defecto, a quien tenga su cuidado personal por sentencia judicial previa. </p>
							<p></p>
							<p></p>
