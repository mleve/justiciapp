						 <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-2">¿Qué es la adopción?</a>
					</div>
					<div id="panel-element-2" class="panel-collapse collapse in">
						<div class="panel-body">
							<p>El acto de recibir legalmente como propio a un hijo que biológicamente no lo es, para brindarle el afecto y los cuidados necesarios para satisfacer sus necesidades espirituales y materiales.</p>
