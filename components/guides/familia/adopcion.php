					<div class="panel-heading">
						 <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-9">¿Cómo adoptar un niño(a)?</a>
					</div>
					<div id="panel-element-9" class="panel-collapse collapse">
						<div class="panel-body">
							<p>Los interesados deben acercarse al Servicio Nacional de Menores o a alguno de los organismos acreditados ante éste, para formar parte de la nómina de personas interesadas en la adopción, la que distingue entre aquellos que residen en el país y los que no.</p>
							<p>Este programa de adopción debe procurar al niño una familia responsable. Debe brindar apoyo y orientación a su familia de origen, recibir al niño y cuidarlo. También hará la evaluación técnica de los solicitantes y la preparación de éstos como familia adoptiva.</p>
							<p>Previo al trámite de la adopción, el niño(a) debe haber sido declarado(a) susceptible de ser adoptado(a), a través de un procedimiento judicial, en tribunales de familia, que constata la situación del niño(a) en relación a su familia de origen, y contempla la posibilidad de que ésta se oponga y haga valer sus derechos. Dicho procedimiento  puede iniciarse de oficio por el juez, a solicitud del Servicio Nacional de Menores</p></div></div></div>
				<div class="panel panel-default">
					<div class="panel-heading">
						 <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-10"> o el organismo acreditado bajo cuyo cuidado permanece el niño(a), o bien por las personas naturales o jurídicas que lo tengan a su cargo.</a>
					</div>
					<div id="panel-element-10" class="panel-collapse collapse">
						<div class="panel-body">
							<p>El trámite de adopción debe realizarse ante el Juzgado de familia del domicilio del niño a solicitud de los interesados que cumplan los requisitos legales, presentando:  </p>
							<p>i. Copia íntegra de la inscripción de nacimiento de la persona que se pretende adoptar, la cual se solicita en el Servicio de Registro Civil</p>
							<p>ii. Copia autorizada, esto es con timbre del tribunal y firma del juez, de la resolución judicial que declara que el niño puede ser adoptado.</p>
							<p>iii. Informe de evaluación de idoneidad física, mental, psicológica y moral del o los solicitantes, emitido por el Servicio Nacional de Menores   o alguna organización acreditada ante él.  
</p>
							<p>Luego comienza un proceso donde el juez oye a los interesados en adoptar, al adoptado y al Sename si procede.</p></div></div></div>
				<div class="panel panel-default">
					<div class="panel-heading">
						 <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-11">¿Debe el juez oir al niño(a)?</a>
					</div>
					<div id="panel-element-11" class="panel-collapse collapse">
						<div class="panel-body">
							<p> En todos los procedimientos de la adopción, tanto en los previos como en el de adopción propiamente tal, el juez debe tener en cuenta la opinión del niño(a), en función de su edad y madurez, y si es mayor de 12 (si es niña) o de 14 (si es niño), se requerirá su consentimiento.</p>
