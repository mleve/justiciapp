						 <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-7">¿Qué niños se pueden adoptar?</a>
					</div>
					<div id="panel-element-7" class="panel-collapse collapse">
						<div class="panel-body">
							<p>Los niños y niñas menores de 18 años:</p>
							<p>a) Cuyos padres no puedan hacerse cargo de su cuidado y expresen el deseo de entregarlo en adopción ante un juez de familia competente.</p>
							<p>b) Que sean descendiente consanguíneo de uno de los adoptantes.</p>
							<p>c) Que hayan sido declarados susceptibles de ser adoptados por resolución de un juez de familia debido a alguna de las siguientes causas: </p>
							<p>Que los padres se encuentren inhabilitados física o moralmente para ejercer su cuidado.</p>
							<p>Que los padres no le proporcionen atención personal o económica durante 2 meses. Este plazo se reduce a 30 días, si el niño(a) es menor de 1 año. No es causa suficiente la falta de recursos económicos.</p>
							<p>Que lo entreguen a una institución pública o privada de protección de niños o a un tercero, con ánimo manifiesto de liberarse de sus obligaciones legales, lo que ocurrirá cuando la mantención del niño a cargo de la institución no obedezca a una causa justificada o no lo visiten</p></div></div></div>
