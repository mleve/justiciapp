					<div class="panel-heading">
						 <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-6">¿Qué requisitos generales hay que cumplir para poder adoptar?</a>
					</div>
					<div id="panel-element-6" class="panel-collapse collapse">
						<div class="panel-body">
							<p>a) En el caso de los cónyuges, deben tener dos o más años de matrimonio civil, salvo que uno o ambos cónyuges sean infértiles (situacion que debe acreditarse), caso en el cual no se exige una duración mínima del matrimonio,</p>
							<p>b) Los interesados deben ser mayores de 25 años de edad y menores de 60, a menos que uno de los adoptantes sea ascendiente consanguíneo del adoptado, por ejemplo la abuela, caso en que no se exigen dichos límites de edad.</p>
							<p>c) Tener una diferencia mínima de 20 años de edad con el adoptado, requisito al que se aplica la misma excepción anterior.</p>
							<p>d) Haber sido evaluados como idóneos desde el punto de vista físico, mental, psicológico y moral, por el Servicio Nacional de Menores (Sename) o algún organismo acreditado ante él para desarrollar programas de adopción.</p>
