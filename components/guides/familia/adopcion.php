						 <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-15">¿Cuánto dura el trámite de adopción?
</a>
					</div>
					<div id="panel-element-15" class="panel-collapse collapse">
						<div class="panel-body">
							<p>Según datos del Sename, desde el momento en que los solicitantes son evaluados como idóneos, ingresando al registro respectivo, el tiempo promedio de espera hasta ser considerados como alternativa de familia para un(a) niño(a), es de 12 a 18 meses aproximadamente.  Sin perjuicio de lo anterior, una vez seleccionada una familia para un niño, normalmente se le confía su cuidado personal provisorio en un plazo que varía dependiendo de la disponibilidad del Tribunal para fijar la respectiva audiencia (aproximadamente 10 días), luego de lo cual debe tramitarse el procedimiento de adopción, en un plazo igualmente variable, dependiendo de la fijación de las audiencias que procedan y del cumplimiento de las gestiones que ordena la sentencia judicial que otorga la adopción, entre las cuales destaca la cancelación de la inscripción de nacimiento original del niño, y la nueva inscripción civil como hijo del o los adoptantes, momento desde el cual la adopción produce sus efectos.</p>
