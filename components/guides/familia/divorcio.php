            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-2">¿Qué es el divorcio?</a>
                </div>
                <div id="panel-element-2" class="panel-collapse collapse in">
                    <div class="panel-body">
                        <p>Según la ley, el divorcio se define como un mecanismo de disolución del vínculo matrimonial, por sentencia judicial del tribunal de familia competente,  que extingue, en general, los derechos y deberes personales y patrimoniales, entre ellos. El divorcio rige para todas las uniones matrimoniales, incluso las celebradas con anterioridad a la fecha en que la ley entró en vigencia.</p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-3">¿En qué se diferencia la nueva Ley de Matrimonio Civil de la antigua?</a>
                </div>
                <div id="panel-element-3" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>La nueva ley regula situaciones que la ley anterior no contemplaba, como la posibilidad de divorciarse y volver a casarse. Si bien en la ley antigua el divorcio estaba regulado, éste no ponía término al matrimonio (ya que no disolvía el vínculo) y por lo tanto no permitía a los divorciados volver a casarse.</p>
                        <p>Se creó una nueva figura denominada compensación económica, a la que tiene derecho el cónyuge (marido o mujer) que durante el matrimonio no trabajó remuneradamente, o trabajó menos de lo que podía y quería, por dedicarse al cuidado de los hijos o a las labores del hogar común. Esta compensación se puede solicitar en los casos de divorcio o nulidad del matrimonio, puede ser acordada por la pareja y autorizada por el juez de familia o, a falta de acuerdo, puede ser determinada por el juez, quien para fijar su monto deberá tomar en cuenta, entre otros aspectos: la duración del matrimonio y vida en común de los cónyuges; la situación patrimonial de ambos; la edad, estado de salud, situación previsional y las posibilidades de reintegrarse al mercado laboral del cónyuge más débil.
                        </p>
                        <p>La compensación económica puede pagarse mediante la entrega de una suma de dinero (una o varias cuotas), acciones u otros bienes, o bien mediante la constitución de derechos de usufructo, uso o habitación sobre bienes de propiedad del cónyuge deudor.</p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-4">¿Cómo se termina legalmente un matrimonio con la nueva ley?</a>
                </div>
                <div id="panel-element-4" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Por cuatro causas :</p>
                        <p>i. La muerte natural de alguno de los cónyuges.</p>
                        <p>ii. La muerte presunta de alguno de los cónyuges (una persona que ha desaparecido tanto tiempo que se piensa está muerta). La muerte presunta debe ser declarada por un juez cumplidos los plazos que establece la ley. </p>
                        <p>iii. Por sentencia firme de nulidad: se declara que el matrimonio jamás existió por no cumplirse los requisitos que señala la ley.</p>
                        <p>iv. Por sentencia firme de divorcio: significa que sí existió matrimonio, pero se le puso término.</p>
                        <p>Tanto la nulidad como el divorcio se inician por medio de una demanda que da inicio a un juicio, que se tramita ante los Tribunales de familia.</p>




                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-5">¿Cuáles son las causales para pedir el divorcio?
                    </a>
                </div>
                <div id="panel-element-5" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Existen dos grupos de causales:</p>
                        <p>i. El cese de la convivencia: Es decir, que los cónyuges no hagan vida en común. En este caso el divorcio puede ser solicitado por ambos de común acuerdo, o bien sólo por uno de ellos.</p>
                        <p>Ambos cónyuges pueden pedir de común acuerdo el divorcio. Para ello debe haber transcurrido al menos un año desde el término de la vida en pareja, lo que debe ser acreditado en el juicio. Si el matrimonio se celebró después de la entrada en vigencia de la nueva Ley de Matrimonio Civil, esto es el 2004, el cese de la convivencia deberá acreditarse con las limitaciones señaladas en la citada ley, las que no rigen para los matrimonios celebrados con anterioridad a ella. Las partes deberán acompañar a su demanda un acuerdo que regule sus relaciones mutuas y respecto de sus hijos y bienes.</p>
                        <p>Sólo uno de los cónyuges puede solicitar el divorcio, sin el acuerdo del otro. Para ello deben haber transcurrido al menos tres años desde el cese de la convivencia en pareja, lo que debe ser acreditado en el juicio. Si el matrimonio se celebró después de la entrada en vigencia de la nueva Ley de Matrimonio Civil, el cese de la convivencia deberá acreditarse con las limitaciones señaladas en la citada ley, las que no rigen para los matrimonios celebrados con anterioridad a ella. Lo relativo a las relaciones mutuas de los cónyuges, a sus hijos y bienes, será regulado en el juicio. En estos casos el juez puede negar el divorcio si el cónyuge que lo pide incumplió su obligación de alimentos, durante le cese de la convivencia.</p>
                        <p>ii. Conductas que infrinjan gravemente los deberes y obligaciones propias del matrimonio, o los deberes y obligaciones que se tienen respecto de los hijos, que tornen intolerable la vida en común. Por ejemplo:</p>
                        <p>- Maltrato físico o psicológico grave, contra el cónyuge o los hijos.</p>
                        <p>- Atentado contra la vida del cónyuge o hijos.</p>
                        <p>- El incumplimiento grave y reiterado de los deberes de convivencia, socorro y fidelidad. Como por ejemplo el abandono reiterado del hogar común.</p>
                        <p>- Conducta homosexual de uno de los cónyuges.</p>
                        <p>- Alcoholismo o drogadicción que impida gravemente una convivencia armoniosa entre los cónyuges o entre éstos y los hijos.</p>
                        <p>Tratándose de esta causal, el divorcio lo puede solicitar el cónyuge afectado sin necesidad de esperar plazo alguno.</p></div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-6">¿El divorcio debe ser patrocinado (tramitado) por un abogado? </a>
                </div>
                <div id="panel-element-6" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Sí, en estos juicios se necesita siempre un abogado para demandar y comparecer ante los Tribunales de Familia, salvo que el juez en caso necesario haga una excepción por motivos fundados (artículo 18, Ley N° 19.968).</p>









                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-7">¿Cuánto demora el procedimiento de divorcio?</a>
                </div>
                <div id="panel-element-7" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> La Ley N° 19.968 que crea los Tribunales de familia establece el procedimiento aplicable  , y ordena al juez adoptar las medidas necesarias para llevar el juicio a término con la mayor celeridad. De la misma forma, dicha ley ordena citar a audiencia preparatoria en el más breve plazo posible. Sin embargo, el tiempo que pueden demorar estos procedimientos judiciales es variable y dependerá de factores como la carga de trabajo del tribunal y la rapidez con que se encuentre y notifique al demandado. También dependerá de los temas que se discutan en el juicio; por ejemplo, si ambas partes solicitan el divorcio de común acuerdo, deberán acompañar un acuerdo completo y suficiente que regule sus relaciones mutuas y las referidas a sus hijos, lo que, de ser aprobado por el juez, podría reducir el tiempo de tramitación del proceso.   </p>









                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-8">¿Es lo mismo la separación judicial que el divorcio?</a>
                </div>
                <div id="panel-element-8" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> No. A diferencia del divorcio, la separación judicial no pone término al matrimonio. Puede ser demandada por uno de los cónyuges por falta imputable del otro, en los casos que prevé la ley y también puede ser solicitada cuando ha cesado la vida en común, con el objeto de regular las relaciones mutuas y con respecto a sus hijos, sin poner término al matrimonio.</p>









                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-9">¿Cómo se comprueba ante la Justicia el cese de la convivencia?</a>
                </div>
                <div id="panel-element-9" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Los matrimonios celebrados después del 17 de noviembre de 2004, deben acreditar el cese efectivo de la convivencia mediante determinados instrumentos, tales como:</p>
                        <p>- Escritura pública o acta extendida y protocolizada ante notario público donde se deje constancia del término de la convivencia.</p>
                        <p>- Acta extendida ante el Oficial del Registro Civil en la que se deje la misma constancia, la cual debe ser notificada al otro conyugue.</p>
                        <p>- Copia de una transacción aprobada judicialmente, por ejemplo, en la que las partes regulan el pago de una pensión de alimentos en beneficio de uno de los cónyuges o de los hijos.</p>






                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-10">¿Por qué conviene más un divorcio por “mutuo acuerdo”?</a>
                </div>
                <div id="panel-element-10" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Porque son las mismas partes, y no un tercero (el juez) las que regulan sus relaciones mutuas, las relativas a sus hijos y a sus bienes. El juez aprobará el acuerdo siempre que cumpla con los requisitos que establece la ley. Además, a diferencia del divorcio unilateral, se puede solicitar después de un año del término de la vida en pareja y la tramitacion judicial tienede a ser mas expedita.</p>









                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-11">Luego de divorciarse o anularse, ¿es posible volver a casarse?</a>
                </div>
                <div id="panel-element-11" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Sí. Para casarse nuevamente la persona tiene que estar divorciada o anulada. Es decir, debe existir la sentencia ejecutoriada de un juez, ya sea de nulidad o de divorcio.</p> 
                        <P> La sentencia deberá subinscribirse al margen de la inscripción matrimonial en el Registro Civil. Sólo después de verificada esa subinscripción, los cónyuges adquirirán el estado civil de divorciados o solteros, según el caso, pudiendo volver a contraer matrimonio.</p>
                        <p> Sin embargo, existen ciertas diligencias que debe cumplir quien desea contraer segundad nupcias, entre ellas:</p>
                        <ul>
                            <li>La mujer que se ha divorciado o se ha declarado nulo su matrimonio y se encuentra embarazada no puede casarse nuevamente antes del parto. Si no hay señales de embarazo, debe esperar 270 días contados desde la disolución o declaración de nulidad del matrimonio para volver a casarse, salvo que se le autorice judicialmente, en cuyo caso deberá acreditar que no hay embarao. Esto está previsto para evitar confusión de paternidades.</li>
                            <li>Quien tiene hijos de matrimonio anterior bajo su patria potestad o bajo su tutela o curaduría debe realizar un inventario solemne de los bienes que se esté administrando y les pertenezcan como herederos de su cónyuge difunto o con cualquiera otro título y dar a los hijos, para estos efectos, un curador especial.</li>
                        </ul> 
                    </div>
