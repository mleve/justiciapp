						 <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-3">¿Qué materias tratan los tribunales laborales?  </a>
					</div>
					<div id="panel-element-3" class="panel-collapse collapse">
						<div class="panel-body">
							<p>Controversias entre empleadores y trabajadores por la aplicación de normas laborales o derivadas de la aplicación de un contrato de trabajo individual o colectivo, convenciones y fallos arbitrales en materia laboral.</p>
							<p>Controversias derivadas de la aplicación de normas referidas a organización sindical y negociación colectiva.</p>
							<p>Juicios relativos a la aplicación de normas de previsión o de seguridad social, presentados por trabajadores activos, empleadores y pensionados (salvo lo relativo a la revisión de las resoluciones sobre la declaración de invalidez o del otorgamiento de licencias médicas).</p>
							<p>Reclamaciones que procedan en contra de las resoluciones dictadas por la autoridad administrativa en materia laboral, previsional o de seguridad social.</p>
							<p>Juicios donde se pretenda hacer efectiva la responsabilidad del empleador derivada de accidentes de trabajo o enfermedades profesionales, con excepción de la responsabilidad extracontractual</p>
                                                        <p>Todas las demás materias que las leyes entreguen a la competencia de los juzgados de letras con competencia laboral.</p>
