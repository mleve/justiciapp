					<div class="panel-heading">
						 <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-8">¿Cuál es el procedimiento de los tribunales laborales?
</a>
					</div>
					<div id="panel-element-8" class="panel-collapse collapse">
						<div class="panel-body">
							<p>Se distinguen cinco tipos de procedimientos: </p>
							<p>   A. Aplicación general El juicio se inicia con una demanda escrita y el juez fija una audiencia en no más de 35 días donde llama a las partes a conciliarse. Si no se logra, el juez determinará cuáles son los puntos de prueba y las partes ofrecerán sus medios de prueba. En esa instancia, el juez puede disponer medidas cautelares, despacho de oficios o citaciones y fijará  la fecha de una segunda audiencia en un plazo no superior a 30 días. En esta segunda audiencia se rinden las pruebas y el juez puede dictar sentencia al término de la audiencia o dentro de 15 días.</p>
							<p>   B. Procedimiento de tutela laboral Se establece un procedimiento especial en caso que se afecten los derechos fundamentales de los trabajadores, entre éstos, el derecho a la vida y a la integridad síquica; el respeto y protección a la vida privada y pública y a la honra de la persona y de su familia; la inviolabilidad de toda forma de comunicación privada; la libertad de conciencia; la libertad para emitir opinión; la libertad del trabajo y el derecho a su libre elección, como también aquellos actos discriminatorios a que se refiere el articulo 2 del codigo del trabajo. Podrán ejercer esta acción de tutela laboral el trabajador o una organización sindical.</p>
							<p>La tramitación de este procedimiento goza de preferencia respecto de todas las demás causas que se tramiten ante el tribunal.</p>
							<p>     C. Procedimiento monitorio Se aplicará en aquellas controversias cuya cuantía sea igual o inferior a 10 ingresos mínimos mensuales y las derivadas del fuero maternal. Se inicia por reclamo ante la Inspección del Trabajo, la cual citará a comparendo a las partes. En el caso de conflicto por fuero maternal, el procedimiento se inicia directamente en el tribunal. </p>
							<p>A la audiencia en la Inspección del Trabajo se debe concurrir con todos los medios de prueba y de no existir conciliación, el trabajador podrá interponer una demanda por escrito ante el juez, dentro de un plazo de 60 días, después de que fue despedido.</p>
							<p> El juez acogerá las pretensiones del demandante de inmediato si las estima fundadas o en caso contrario, las rechazará de plano. En caso de no existir antecedentes suficientes para este pronunciamiento, o si las partes reclaman de esta resolución, el tribunal deberá citar a una audiencia única de conciliación y prueba, que deberá celebrarse dentro de los 15 días siguientes a la presentación de la demanda. Las partes deberán asistir a esta audiencia con todos sus medios de prueba y en ella el juez deberá dictar sentencia. </p>
							<p>       D. Procedimiento de reclamación de multas y demás resoluciones administrativas  Se establece que el reclamo de multas administrativas se regirá por el procedimiento de aplicación general, debiendo interponerse el reclamo en el plazo de 15 días hábiles contados desde la notificación de la sanción.</p>
							<p>      E. Procedimiento Ejecutivo  Se trata de un procedimiento forzado o ejecutivo, es decir, se trata de una sentencia que se debe hacer cumplir.</p></div></div></div>        </div>
