        <h2>
            Preguntas frecuentes en materia de Accidentes laborales
        </h2>
        <h4>Leyes relacionadas con esta materia:
            <a href="http://www.leychile.cl/Consulta/m/m/norma_plana?idNorma=28650&org=bleyes_r?t_n=XX1&nro_ley=16744&orga=&f_pub=" target="_blank">
                Ley Nº 16.744, sobre accidentes del trabajo y enfermedades profesionales. </a></h4>
    </div>
</div>
<div class="row clearfix">
    <div class="col-md-12 column">
        <div class="panel-group" id="panel-questions">

            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-2">¿Qué se entiende por accidente del trabajo?
                    </a>
                </div>
                <div id="panel-element-2" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Se reconoce como accidente laboral toda lesión que sufre una persona a causa o con ocasión de su trabajo y que le produzca lesiones de incapacidad o muerte. Éstos pueden ocurrir en actividades gremiales, de capacitación ocupacional o en el desarrollo de cualquier actividad.  También se incluyen los accidentes que ocurran en el trayecto de ida o regreso entre la casa y el lugar de faena, trabajo o viceversa.  El trayecto entre dos lugares de trabajo también se considera accidente del trabajo, pero es de responsabilidad del trabajo de destino.</p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p></div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-3">¿Qué tipo de funcionarios o trabajadores están incluidos? </a>
                </div>
                <div id="panel-element-3" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Trabajadores dependientes.</p>
                        <p>Funcionarios públicos de la administración del Estado y municipal.</p>
                        <p>Trabajadores civiles de ASMAR, FAMAE y ENAER.</p>
                        <p>Estudiantes que deban ejecutar trabajos que signifiquen fuentes de ingreso para el plantel donde estudian.</p>
                        <p>Los trabajadores independientes y los trabajadores familiares.</p>
                        <p>Alumnos en práctica.</p></div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-4">¿Qué debo hacer si sufro un accidente de trabajo?</a>
                </div>
                <div id="panel-element-4" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Informar a su empleador para que éste lo derive inmediatamente, para su atención, al establecimiento asistencial del organismo administrador que le corresponda.  En el evento que el empleador no cumpla con la obligación o cuando las circunstancias del caso impiden que el empleador tome conocimiento, el trabajador podrá recurrir por sus propios medios y deberá ser atendido de inmediato.</p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p></div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-5">¿Dónde se debe comunicar el incidente?</a>
                </div>
                <div id="panel-element-5" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>
                            A la Mutual a la que está adherida la empresa o al hospital más cercano. Habitualmente en las empresas esta labor recae en el departamento de Recursos Humanos o Bienestar. La Mutual o el hospital emitirá un dictamen que el trabajador presentará en la institución aseguradora o en el IPS, según sea el caso.</p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p></div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-6">¿Qué pasa si el accidente es muy grave o fatal?</a>
                </div>
                <div id="panel-element-6" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> El empleador deberá informar los hechos inmediatamente a la Inspección del Trabajo y a la Secretaría Regional Ministerial de Salud que corresponda. Además, el empleador deberá suspender en forma inmediata las faenas, y de ser necesario, permitir a los trabajadores la evacuación del lugar de trabajo.</p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p></div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-7">¿Qué pasa si me accidento en un paseo o actividad recreativa de la empresa?</a>
                </div>
                <div id="panel-element-7" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>
                            Ninguna de estas dos actividades está estipulada dentro de las causas u ocasiones de un accidente del trabajo. Según la ley, es considerado un accidente laboral toda lesión que una persona sufra a causa o con ocasión del trabajo, y que le produzca lesiones, incapacidad o muerte. Sin embargo, si la actividad o paseo recreativo es de asistencia obligatoria, se considerará que se cumple con los requisitos para ser calificado como accidente del trabajo.</p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p></div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-8">¿Qué pasa si me accidento por las malas condiciones de trabajo?
                    </a>
                </div>
                <div id="panel-element-8" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>El siniestro es considerado accidente del trabajo. Aquellas empresas o entidades que no ofrezcan condiciones de seguridad e higiene, deberán pagar la cotización adicional con un recargo del 100%, además de las sanciones que les correspondan.</p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p></div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-9">¿Cuál es la indemnización que recibo por la perdida de una extremidad?
                    </a>
                </div>
                <div id="panel-element-9" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>En caso de que la persona sufra la mutilación de alguno de sus miembros, sin incapacitarlo para trabajar, recibirá una indemnización.  Si ha sufrido una pérdida igual o superior a un 15% de sus capacidades, pero inferior a un 70%, el trabajador tendrá derecho a una indemnización global de un máximo de 15 veces el sueldo base. En ningún caso la indemnización podrá ser inferior a medio sueldo vital mensual del departamento de Santiago.</p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p></div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-10">¿Qué cubre la institución aseguradora? </a>
                </div>
                <div id="panel-element-10" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Prestaciones preventivas: son acciones orientadas a la prevención de riesgos laborales.</p>
                        <p>Prestaciones médicas: cubren la atención médica, quirúrgica y dental en establecimientos externos o en atención domiciliaria; hospitalización si fuere necesario; medicamentos y productos farmacéuticos; prótesis y aparatos ortopédicos y su reparación; rehabilitación física y reeducación profesional, los gastos de traslado y cualquier otro que sea necesario para el otorgamiento de las prestaciones.</p>
                        <p>Prestaciones económicas: si el trabajador queda con incapacidad temporal, tendrá derecho a un subsidio que reemplazará la remuneración que dejará de percibir mientras esté incapacitado. Este corresponde al promedio de las tres últimas remuneraciones mensuales imponibles anteriores al evento.</p>
