                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-2">¿Cuáles son las causales legales para poner término a un contrato de trabajo? </a>
                </div>
                <div id="panel-element-2" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Muerte del trabajador.</p>
                        <p>Mutuo acuerdo entre las partes.</p>
                        <p>Renuncia voluntaria.</p>
                        <p>Vencimiento del plazo convenido en el contrato.</p>
                        <p>Fin del trabajo o servicio.</p>
                        <p>Caso fortuito o fuerza mayor.</p>
                        <p>Conducta indebida de carácter grave, la que debe ser debidamente comprobada. Por ejemplo, falta de probidad del trabajador, acoso sexual o conducta inmoral.</p>
                        <p>Negociaciones que ejecute el trabajador dentro del negocio y que aparecen como prohibidas en el contrato.</p>
                        <p>No concurrencia del trabajador a sus labores sin causa justificada durante dos días seguidos, o dos lunes en el mes, o un total de tres días en el mes.</p>
                        <p>Abandono del trabajo por parte del trabajador.</p>
                        <p>Actos, omisiones o imprudencias temerarias que afecten el funcionamiento del establecimiento, la seguridad o la actividad de los trabajadores.</p>
                        <p>Daño material causado intencionalmente en las instalaciones, maquinarias, herramientas, útiles de trabajo, productos o mercaderías</p>
                        <p>Incumplimiento grave de las obligaciones que impone el contrato.</p>
                        <p>Por necesidades de la empresa, establecimiento o servicio.</p>
                        <p>Esto según lo establecen los artículos 159, 160 y 161 del codigo del trabajo.</p>
                    </div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-4">¿Cómo se comunica la terminación del contrato?
                    </a>
                </div>
                <div id="panel-element-4" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>A través de una carta, que el empleador debe entregar personalmente o por correo certificado, con copia a la Inspección del Trabajo respectiva, que contenga lo siguiente: </p>
                        <p>la causal legal aplicada.</p>
                        <p>los hechos en los que se funda.</p>
                        <p>el monto de las indemnizaciones, por término de contrato, si correspondiere.</p>
                        <p>estado en que se encuentren las imposiciones del trabajador, hasta el último día del mes anterior al despido, adjuntando comprobantes.</p>
                        <p> Tratándose de la terminación del contrato por la causal “necesidades de la empresa”, debe indicarse el monto a pagar por concepto de indemnizaciones por años de servicio.</p>
