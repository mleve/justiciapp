                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-5">¿Qué plazos otorga la ley para comunicar el despido?
                    </a>
                </div>
                <div id="panel-element-5" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Cuando se invoque la causal “necesidades de la empresa”, se debe comunicar con al menos 30 días de anticipación.</p>
                        <p>Si venció el plazo del contrato, finalizó el trabajo para el que fue contratado o por las causales disciplinarias del artículo 160, se debe informar dentro delos 3 días siguientes desde que deja de prestar servicio.</p>
                        <p>Si la causal de despido es caso fortuito o fuerza mayor, se debe comunicar dentro de los 6 días siguientes desde que deja de prestar servicio.</p>
