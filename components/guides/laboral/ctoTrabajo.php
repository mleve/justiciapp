            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-12">¿Puede esgrimirse una catástrofe natural (terremoto, tsunami, etc.) como argumento para despedir personal sin pago de indemnizaciones?
                    </a>
                </div>
                <div id="panel-element-12" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>El Código del Trabajo en su artículo 159, establece que un contrato de trabajo puede terminarse antes del tiempo, sin considerar pago de indemnizaciones, entre otras causales, por casos “fortuitos o fuerza mayor”. Por su parte el artículo 45 del Código Civil define que se entiende por caso fortuito y mayor: “Se llama fuerza mayor o caso fortuito el imprevisto a que no se es posible resistir como un naufragio, un terremoto, el apresamiento de enemigos, los actos de autoridad ejercidos por funcionarios públicos, etc.”.</p>
                        <p>A propósito del terremoto del 27 de febrero de 2010, la Dirección del Trabajo, en su Dictamen N° 1412 021 del 19 de marzo de 2010, aclaró que para la aplicación estricta de la causal “caso fortuito o fuerza mayor”, deben cumplirse las siguientes condiciones:</p>
                        <p>a) Que los daños ocurridos en las instalaciones de la empresa se deban causalmente a la ocurrencia del terremoto. El sismo debe ser causa directa. </p>
                        <p>b) Que el empleador que invoque esta causal no puede haber contribuido al acaecimiento del mismo y/o a sus efectos lesivos. Es decir, el empleador no puede haber contribuido, sea por acción u omisión, a los daños que gatilló el sismo. Por ejemplo, no podría invocarse esta causal de fuerza mayor si el empleador mantenía en mal estado el edificio donde se ubicaba la empresa, si no efectuó reparaciones que sabía que tenían que hacerse, o si existía peligro de derrumbe aun sin que ocurriera un terremoto. </p>
                        <p>c) Que el terremoto no se haya podido prever dentro de cálculos ordinarios o corrientes. Es decir, que el sismo sea una contingencia imposible de prever. Es sabido que el nuestro es un país sísmico y que es parte del conocimiento popular que ocurrirán temblores con cierta frecuencia, y por ende existen seguros contra sismos; pero aún así es imposible saber con certeza el día en que ocurrirá un terremoto, ni su magnitud. </p>
                        <p>d) Que el terremoto y sus efectos directos sean irresistibles, o sea, que suponga la nula posibilidad de mantener el puesto de trabajo de los trabajadores y, por ende, de cumplir con las obligaciones contractuales del empleador. Por ejemplo, si el único local de una empresa fue completamente destruido y, por ende, tal empresa deja de existir. Caso contrario es cuando una empresa tiene distintos locales o sucursales, pues existe la posibilidad de reubicar a su personal.</p>
