                                            <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-7">¿Cuánto tiempo ampara a la mujer trabajadora su fuero maternal? </a>
					</div>
					<div id="panel-element-7" class="panel-collapse collapse">
						<div class="panel-body">
							<p>En razón de lo que dispone el artículo 201 del Código del Trabajo, la mujer trabajadora  goza de fuero durante el período de embarazo y hasta un año después de expirado el descanso de maternidad. </p>
