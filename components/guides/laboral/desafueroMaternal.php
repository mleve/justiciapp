				<div class="panel panel-default">
					<div class="panel-heading">
						 <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-4"> ¿Qué es el desafuero maternal?</a>
					</div>
					<div id="panel-element-4" class="panel-collapse collapse">
						<div class="panel-body">
                                                    <p> El <strong>desafuero</strong> es el procedimiento judicial mediante el cual el empleador obtiene la autorización para despedir a un trabajador aforado o con fuero maternal u otro tipo. La finalidad del procedimiento es obtener una autorización de parte del juez para poner término a un contrato de trabajo. En el caso de la trabajadora embarazada la acción se denomina de desafuero maternal. </p>
							
                                                </div>
                                        </div>
                                </div>
				<div class="panel panel-default">
					<div class="panel-heading">
						 <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-5">¿Causales que autorizan al empleador a solicitar el desafuero maternal?</a>
					</div>
					<div id="panel-element-5" class="panel-collapse collapse">
						<div class="panel-body">
							<p>Las causales que autorizan a solicitar el desafuero maternal, son taxativas, esto es no hay más que las que se señalan:</p>
                                                        <ul>
                                                            <li>Vencimiento del plazo convenido por las partes (159 Nº4)</li>
                                                            <li>Conclusión del trabajo o servicio que dio origen al contrato (159 Nº5)</li>
                                                            <li>Causales por hechos imputables a la conducta del empleador.(160)</li>
                                                        </ul>
                                                </div>
                                        </div>
                                </div>
				<div class="panel panel-default">
					<div class="panel-heading">
						 <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-6">¿Qué sucede si se despide a una mujer amparada por fuero maternal sin la autorización de un juez?</a>
					</div>
					<div id="panel-element-6" class="panel-collapse collapse">
						<div class="panel-body">
							<p>Según nos indica el  inciso 4° del artículo 201 del Código del Trabajo establece que la medida quedará sin efecto, y la trabajadora volverá a su trabajo, para la cual sólo bastará la presentación del correspondiente certificado médico o de matrona, agregando que la afectada deberá hacer efectivo este derecho dentro del plazo de 60 días hábiles contados desde el despido. </p>
							
                                                </div>
