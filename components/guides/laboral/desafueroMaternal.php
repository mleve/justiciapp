				<div class="panel panel-default">
					<div class="panel-heading">
						 <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-3">¿Qué tipo de empresas deben cumplir con la protección a la maternidad?</a>
					</div>
					<div id="panel-element-3" class="panel-collapse collapse">
						<div class="panel-body">
							<p>Todas las empresas e instituciones públicas y privadas, incluso los centros comerciales y malls. Y  en todas las sucursales o faenas que posea el establecimiento, empresa o servicio.</p>
							
