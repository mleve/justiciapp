					<div class="panel-heading">
						 <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-2">¿Qué es el fuero maternal?</a>
					</div>
					<div id="panel-element-2" class="panel-collapse collapse in">
						<div class="panel-body">
							<p> Es la protección que se le otorga a la mujer embarazada y tiene por objeto mantener el empleo de la mujer para que ésta tenga asegurado el origen de sus ingresos y pueda alimentar y criar a su hijo/a.</p>
							<p>Esta es una protección que la ley otorga a la maternidad y se expresa en la prohibición de un empleador de poner término al contrato de trabajo, sin la autorización previa del juez competente, es decir, sólo una sentencia judicial, podrá autorizar el despido de una trabajadora sujeta a fuero maternal.</p>
							<p>Se extiende desde el momento del embarazo hasta 1 año después de expirado el periodo del postnatal, es decir cuando el/la hijo/a ha cumplido 1 año y 84 días.</p>
							<p>Permite que la trabajadora tenga derecho a ser reintegrada a su trabajo si su empleador la despide estando embarazada.</p>
                                                </div>
                                        </div>
