            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-1">
                    ¿Que es el nombre?
                    </a>
                </div>
                <div id="panel-element-1" class="panel-collapse collapse in">
                    <div class="panel-body">
                        <p>
                             Es la denominación por la cual se individualiza a una persona. Está formado por el nombre propio (nombre de pila) y el nombre patronímico o apellido (o de familia). El 				primero es determinado por los progenitores a su libre voluntad, sin embargo el patronímico está ligado a la filiación y revela los orígenes del individuo.
                        </p>
                       </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-2">
                       ¿En qué consiste el cambio de nombre?
                    </a>
                </div>
                <div id="panel-element-2" class="panel-collapse collapse">
                    <div class="panel-body" >
                        <p>
                           Existen dos opciones
                        </p>
			<p>
				a. Cambio de nombre por via Administrativa:  que opera de oficio, es decir por iniciativa propia del Servicio de Registro Civil, o bien  a petición del interesado;  					operando  sólo para quienes presenten omisiones o errores en su partida de nacimiento, por ejemplo en lugar de figurar Marcelo, figura como nombre Narcelo.
			</p>
			<p>
                            b.  Via judicial, denominandose este procedimiento rectificación de la partida de nacimiento y consiste en cambiar nombres, apellidos o ambos.
                        </p>
			</div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-3">
                       ¿Cualquiera puede modificar su nombre?
                    </a>
                </div>
                <div id="panel-element-3" class="panel-collapse collapse">
                    <div class="panel-body" >
                        <p>
                            No, esta facilidad se creó para modificar los nombres y/o apellidos de las personas que se encuentren en las siguientes situaciones: 
                        </p>
                        </p>
                        <p>
                           Que sus nombres sean ridículos, risibles o les impliquen menoscabo moral o material.
                        </p>
                        <p>
                             Haber sido conocido por más de 5 años con otros nombres, por ejemplo el caso del diputado Pepe Auth quien cambio su nombre de Jose a Pepe por este procedimiento.
                        </p>
			     Para agregar un apellido a los hijos cuya filiación ( quien es su padre o madre) no se encuentra determinada o tengan ambos apellidos iguales.
			<p>
                        </p>
			     Cuando las voces no sean de origen español para castellanizarlas o modificar su pronunciación o escritura
                        </p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-4">
                        ¿Cómo puedo realizar el trámite?
                    </a>
                </div>
                <div id="panel-element-4" class="panel-collapse collapse">
                    <div class="panel-body" >
                        <p>
                           Debe contar con un abogado particular o solicitar asesoría gratuita al consultorio de su comuna de la Corporación de Asistencia Judicial para que el|ella realice los 				   trámites ante el juzgado de letras correspondiente a su domicilio.
                        </p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-5">
                      ¿Cuánto cuesta el trámite?
                    </a>
                </div>
                <div id="panel-element-5" class="panel-collapse collapse">
                    <div class="panel-body" >
                        <p>
                         Los honorarios del abogado particular en su caso, más el valor de la documentación exigida.  
                        </p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-6">
                       ¿Cuánto tarda la solicitud?
                    </a>
                </div>
                <div id="panel-element-6" class="panel-collapse collapse">
                    <div class="panel-body" >
                        <p>
                        Debido a que es un procedimiento voluntario ( no existe conflicto con otra parte, por tanto no hay demandado) puede demorarse, en promedio, seis meses aproximadamente, ya que 				dependerá del tribunal.
                        </p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-7">
                      Si modifico mi apellido, ¿ afecta esto los apellidos de mis hijos?
                    </a>
                </div>
                <div id="panel-element-7" class="panel-collapse collapse">
                    <div class="panel-body" >
                        <p>
                         El cambio de apellido se hace extensible a los hijos sujetos a la patria potestad, es decir, aquellos hijos cuyos bienes son administrados por el adulto que los tiene bajo su 			cuidado personal y debe pedirse en el momento de solicitar el cambio. También se hace extensible a los demás descendientes que consientan en ello, por ejemplo los nietos
                        </p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-8">
                        ¿Puedo retractarme de la modificación a mi nombre o apellido o solicitar cambio de nombre mas de una vez?
                    </a>
                </div>
                <div id="panel-element-8" class="panel-collapse collapse">
                    <div class="panel-body" >
                        <p>
                         No, sólo se permite un cambio en la vida.
                        </p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-9">
                       Si cambio mi apellido, pero mis hijos no quieren hacerlo, ¿tendrán problemas para heredar mis bienes?
                    </a>
                </div>
                <div id="panel-element-9" class="panel-collapse collapse">
                    <div class="panel-body" >
                        <p>
                         No, el cambio de nombre no altera la filiación, por lo que sus descendientes heredarán normalmente pues conservan todos sus derechos.
                        </p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-10">
                      ¿Se puede exigir a un arrendatario moroso el pago de cuentas de servicios (luz, agua, gastos comunes y otros)?
                    </a>
                </div>
                <div id="panel-element-10" class="panel-collapse collapse">
                    <div class="panel-body" >
                        <p>
                        Sí, el arrendatario debe pagar todas las cuentas morosas incluso en el período en que dure el juicio, hasta que se pague todo lo adeudado o se entregue la propiedad al arrendador. 
                        </p>
                        <p>
                        El arrendador además puede notificar a las compañías de que hay una demanda en curso, por lo que el demandado queda como único responsable del pago de las cuentas.    
                        </p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-11">
                       Si el deudor es obligado a pagar, ¿debe hacerlo con intereses por el atraso?
                    </a>
                </div>
                <div id="panel-element-11" class="panel-collapse collapse">
                    <div class="panel-body" >
                        <p>
                       Sí, el interés será la variación de la UF entre la fecha en que el pago debió hacerse y la fecha en que efectivamente se hizo.
                        </p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-12">
                       Una vez finalizado el juicio ¿se puede solicitar que el arrendatario moroso abandone la propiedad o vivienda?
                    </a>
                </div>
                <div id="panel-element-12" class="panel-collapse collapse">
                    <div class="panel-body" >
                        <p>
                        El juez puede dictar como sentencia que el arrendatario entregue la propiedad  en un plazo máximo (contando solicitudes de prórroga) de quince días. 
                        </p>
                        <p>
                        Si llegado el día el arrendatario no ha entregado la propiedad, el juez puede ordenar el lanzamiento, que debe ser apoyado por la fuerza pública.     
                        </p>
                        <p>
                        También es posible que el juez declare que la demanda no tiene lugar, en ese caso no se puede presentar una nueva demanda hasta seis meses después, a menos que hayan ocurrido nuevos hechos.    
                        </p>
