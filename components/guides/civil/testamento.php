            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-9">¿Quiénes no pueden hacer un testamento? </a>
                </div>
                <div id="panel-element-9" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>La mujer menor de doce años y el varón menor de catorce años de edad, pues según la ley, son absolutamente incapaces.</p>
                        <p>Las personas que no están en su sano juicio por ebriedad u otra causa.</p>
                        <p>El demente privado de la administración de sus bienes, a quien se le designa un representante legal.</p>
                        <p>Aquellas personas que no puedan expresar claramente su voluntad. </p>
                        <p></p></div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-10">¿Se puede revocar un testamento? </a>
                </div>
                <div id="panel-element-10" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Sí, es revocable mientras viva el testador. Basta con que éste haga uno nuevo, pero es recomendable incluir una cláusula donde quede claro que el documento anterior queda nulo. Sin embargo, sigue siendo válido el reconocimiento de un hijo, aunque el testamento sea revocado con posterioridad.</p>
                        <p></p>
                        <p></p>
