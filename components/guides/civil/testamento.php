            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-11">¿Cómo se hace cumplir un testamento? </a>
                </div>
                <div id="panel-element-11" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>
                            El testador puede haber designado en el testamento un albacea o ejecutor testamentario, que es la persona encargada de hacer cumplir lo que dice el testamento.</p>
                        <p>Tras la muerte del testador, el albacea debe informar a los herederos que existe un testamento y que representa la última voluntad de la persona. Luego él deberá hacer las gestiones necesarias ante los tribunales para que lo que el testamento dice respecto a bienes materiales sea incorporado a la posesión efectiva.  Si el testador no ha designado un albacea, cualquier heredero puede ejecutar el testamento solicitando la posesión efectiva testada ante tribunales, representado por un abogado. </p>
                        <p></p>
                        <p></p>
                        <p></p></div></div></div>
