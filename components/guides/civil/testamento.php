                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-2">¿Qué es un testamento? </a>
                </div>
                <div id="panel-element-2" class="panel-collapse collapse in">
                    <div class="panel-body">
                        <p> Es un acto por el cual una persona (el testador) dispone de todo o parte de sus bienes para que sean heredados después de que fallezca. Según la ley, hay varios tipos de testamento: </p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-3">¿Cómo se hace un testamento? </a>
                </div>
                <div id="panel-element-3" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Primero debe decidir si va a hacer un testamento solemne abierto o cerrado. Los testamentos menos solemnes sólo se dan en casos excepcionales en que corra peligro la vida del testador. </p>
                        <p>Testamento solemne abierto: debe ir donde un notario con tres testigos mayores de 18 años y puede llevar el testamento escrito. El notario deberá leerlo en voz alta ante los tres testigos. En el testamento debe constar el nombre, edad, domicilio y nacionalidad del testador, detalle de sus matrimonios y de todos sus hijos, la declaración de hallarse en pleno uso de sus facultades y el nombre y domicilio de los testigos. El documento debe firmarse por el testador, el notario y los testigos. En caso de que el testamento no se realice ante Notario, podrá hacerse ante cinco testigos, debiendo cumplirse con algunos trámites adicionales con posterioridad a la muerte.</p>
                        <p>Testamento solemne cerrado: se debe llevar a la Notaría el testamento en sobre cerrado, escrito y firmado por el testador. En el sobre deben ponerse los datos y firmas del testador, el notario, los tres testigos y la fecha del otorgamiento. El testador debe decirles, de viva voz, que en el sobre está contenido su testamento.</p>
                        <p>Testamento menos solemne verbal: la ley lo autoriza sólo si no hay tiempo para hacer un testamento solemne. Se hace de viva voz ante al menos tres testigos mayores de 18 años que sepan leer y escribir. Para que tenga valor, deberá ponerse por escrito y cumplir con los requisitos legales dentro del plazo de 30 días contados desde la muerte del testador. Cualquier persona interesada en la herencia puede acudir a un juez de letras, quien debe ubicar a los testigos y tomarles declaración sobre el testamento que declaró el difunto y en qué condiciones lo hizo.</p>
                        <p>Testamento menos solemne militar y marítimo: se pueden hacer en caso de Estado de Guerra cumpliendo con los requisitos legales.</p>
                    </div>
                </div>
            </div>
           <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-5">¿A quiénes puedo dejar mis bienes?</a>
                </div>
                <div id="panel-element-5" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> En Chile los testamentos son restringidos. No existe libertad absoluta para disponer de los bienes ya que la ley obliga al testador a cumplir con asignaciones forzosas a favor de ciertas personas. Las disposiciones del testamento que no las cumpla no tendrán valor. Las asignaciones obligatorias abarcan: </p>
                        <p>Los alimentos forzosos que el difunto debía por ley, deben pagarse con cargo a los bienes de la herencia.</p>
                        <p>Las legítimas que se componen por el 50% de los bienes (mitad legitimaria) y les corresponde a los herederos como el cónyuge, descendientes o ascendientes (padres y abuelos)</p>
                        <p>La cuarta de mejoras se compone del 25% de los bienes y con ella se puede favorecer la cuota de determinados herederos (cónyuge, descendientes  o ascendientes). El 25% restante, llamada "cuarta de libre disposición", puede dejarse libremente a cualquier persona.</p>
                        <p>
                            En resumen, el 50% de los bienes va a los herederos forzosos, el 25% va para mejorar a alguno de los beneficiados por la primera mitad, y el otro 25%, a quien el testador desee.</p></div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-6">¿Qué son las asignaciones forzosas? </a>
                </div>
                <div id="panel-element-6" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> Son aquellas asignaciones que el testador está obligado a hacer y que se suplen cuando no las ha hecho, aún en contra de lo que haya dispuesto en su testamento. Éstas son: i.-Los alimentos que se deben por ley a ciertas personas. ii.-Los legitimarios: los hijos, personalmente o representados por su descendencia; los ascendientes, y el cónyuge sobreviviente. iv.- La cuarta de mejoras para los descendientes, ascendientes y del cónyuge.  </p>
                        <p></p>
                        <p></p>
                        <p></p>
                        <p></p></div></div></div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-7">¿El testamento sólo sirve para transmitir bienes? </a>
                </div>
                <div id="panel-element-7" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>
                            No, también se pueden realizar declaraciones, como por ejemplo, nombrar un guardador para sus hijos o reconocer un hijo no matrimonial. </p>
