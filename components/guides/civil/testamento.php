            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-8">¿Cuál es la diferencia entre herencia y legado? </a>
                </div>
                <div id="panel-element-8" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p> La herencia corresponde a todo o parte del patrimonio del testador. El legado corresponde más a bienes particulares, concretos y específicos, como por ejemplo un auto o una obra de arte. </p>
                        <p></p>
                        <p></p>
