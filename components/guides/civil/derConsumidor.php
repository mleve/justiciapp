            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-8">
                         ¿Qué dice la ley sobre la publicidad engañosa?
                    </a>
                </div>
                <div id="panel-element-8" class="panel-collapse collapse">
                    <div class="panel-body" >
                        <p>
                          La Ley del Consumidor sanciona la publicidad que induce a error o engaño sobre las características del bien o servicio que promueve y establece que los anuncios deben ser 				comprobables.
			<p>
			<p>
			Comete infracción la empresa que induce a error o engaño respecto de: 
			<p>
			<p>
			Los componentes del producto y su porcentaje. 
			<p>
			La idoneidad del bien o servicio para los fines que se pretende satisfacer. 
			<p>
			Las características relevantes del bien o servicio. 
			<p>
			El precio del bien o servicio y su forma de pago. 
			<p>
			Las condiciones de la garantía. 
			<p>
			Su condición de no producir daño al medio ambiente o a la calidad de vida y el ser reciclable y reutilizable. 
			Por ejemplo, un producto que promete bajar de peso en una semana y resulta no ser así o una inmobiliaria que ofrece áreas verdes que no entrega.
			<p>
			<p>
			De esta forma quien incurra en publicidad engañosa recibirá una multa de entre 50 y 750 UTM. Ahora, si los efectos de ese engaño derivan en daños al medio ambiente, la multa 				será de 1.000 UTM. El juez puede doblar la multa en caso de reincidencia.
                        </p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-10">
                       ¿Cómo y donde denunciar a una tienda, proveedor o empresa?
                    </a>
                </div>
                <div id="panel-element-10" class="panel-collapse collapse">
                    <div class="panel-body" >
                        <p>
                         La ley de protección al consumidor establece que el consumidor o usuario afectado por la infracción a las normas de esta ley podrá:
			<p>
			<p>
			a) Denunciar la infracción y mediante ella obtener que el proveedor de bienes o servicios sea sancionado con una multa a beneficio fiscal.
			<p>
			b) Denunciar y demandar, es decir, denunciar la infracción cometida, pero conjuntamente demandar la reparación e indemnización de los daños materiales y morales sufridos con 				ocasión de la infracción. La reparación e indemnización se encuentra establecida como derecho en el artículo 3 letra e) de la presente ley.
			<p>
			La denuncia y/o demanda debe presentarse por escrito, se faculta al consumidor para intervenir en el juicio personalmente sin necesidad de abogado habilitado.
			<p>
			<p>
			Respecto de donde denunciar, debe saber que todas las infracciones a la ley del consumidor deben denunciarse en el Juzgado de Policía Local competente que sera  a elección del 			consumidor:
			<p>
			<p>
			a) Aquel que corresponda a la comuna donde se celebró el contrato.
			<p>
			b) Aquel que corresponda a la comuna donde se hubiese cometido la infracción.
			<p>
			c) Aquel que corresponda a la comuna donde se dio inicio a la ejecución de la infracción.
						</p>
                    </div>
