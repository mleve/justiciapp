                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-1">
                    ¿Quienes son los los consumidores?
                    </a>
                </div>
                <div id="panel-element-1" class="panel-collapse collapse in">
                    <div class="panel-body">
                        <p>
                              Se entiende que son consumidores o usuarios las personas físicas o jurídicas (empresas, corporaciones, iglesias, siandicatos etc) que adquieren, utilizan o disfrutan como 				destinatarios finales, bienes muebles o inmuebles, productos, servicios, actividades o funciones, cualquiera que sea la naturaleza pública o privada, individual o colectiva de 			quienes los producen, facilitan, suministran o expiden
                        </p>
                       </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-2">
                        ¿Por qué como consumidores es necesario pedir una boleta de compra?
                    </a>
                </div>
                <div id="panel-element-2" class="panel-collapse collapse">
                    <div class="panel-body" >
                        <p>
                           La boleta de compra permite ejercer la garantía legal, esto es, el derecho a que cuando un producto nuevo sale malo, se pueda cambiar, reparar o recuperar el dinero. Luego, 			para hacer efectivo este derecho, se  debe presentar la boleta o factura, o cualquier otro documento que acredite la compra (como voucher de tarjeta de crédito o estado de 				cuenta si es cliente de la multitienda).
                        </p>
		     </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-3">
                        ¿Qué hago si el producto o servicio que me vendieron resultó no ser lo prometido o salió defectuoso?
                    </a>
                </div>
                <div id="panel-element-3" class="panel-collapse collapse">
                    <div class="panel-body" >
                        <p>
                            Dependiendo del servicio o bien que adquirió, puede solicitar:  
                        </p>
                        </p>
                        <p>
                            La devolución de su dinero. 
                        </p>
                        <p>
                            El reemplazo del artículo. 
                        </p>
			    Su reparación gratuita o intercambio por otro bien o servicio. 
			<p>
                        </p>
			      Tiene un plazo de tres meses desde que adquirió el producto para reclamar y pedir su compensación ante el vendedor, importador o fabricante, siempre y cuando no sea por 				una falla que haya sido causada por usted (por descuido o mal uso). Los alimentos o productos perecibles tienen como garantía el plazo que indique el envase o un máximo de 7 				días. Esta es una garantía automática que le da la ley. Si usted compró con una póliza de garantía, regirá la que se extienda por más tiempo.
                        </p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-4">
                         ¿ A qué se obligan las partes de un contrato (proveedor y consumidor)?
                    </a>
                </div>
                <div id="panel-element-4" class="panel-collapse collapse">
                    <div class="panel-body" >
                        <p>
                            Tanto las empresas como los consumidores deben respetar lo convenido en los contratos y una de las partes no puede cambiarlo sin consentimiento de la otra. 
                        </p>
                        </p>
			    Recuerde además, que todo lo que se promete en la publicidad pasa a ser parte del contrato tal como si estuviera escrito. 
                        </p>
                        </p>                      
			    No obstante, los contratos no pueden contener cláusulas abusivas, como aquellas en que la empresa se desligue anticipadamente de su responsabilidad ante incumplimientos. 				    Tampoco contener cláusulas en las que una de las partes pueda cambiar las condiciones. 
			</p>
			</p>
			     No son válidos los contratos que incluyan espacios en blanco que no hayan sido llenados o inutilizados antes de firmarlos.
			</p>
			</p>
			     Respecto a la letra chica letra, ella se encuentra prohibida y de hecho la letra de los contratos no puede ser más pequeña que 2,5 milímetros (fuente tamaño 12). Las   				     cláusulas que no se atengan a esta norma, quedarán sin efecto.
			</p>
                    </div>
                </div>
            </div>
