<div class="row clearfix">
    <div class="col-md-12 column">
        <div class="panel-group" id="panel-questions">

            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-2"> ¿Qué es el recurso de protección?
                    </a>
                </div>
                <div id="panel-element-2" class="panel-collapse collapse in">
                    <div class="panel-body">
                        <p>Es aquella acción que la Constitución concede a todas las personas que como consecuencia de actos u omisiones arbitrarias o ilegales, sufren privación, perturbación o amenaza a sus derechos y garantías constitucionales.</p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-3"> ¿Cuáles son los derechos y libertades amparados por el recuro de protección? </a>
                </div>
                <div id="panel-element-3" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Los derechos respecto de los cuales se puede presentar este recurso se encuentran   en el artículo 20 de la Constitución, que establece cuáles de los numerales del artículo 19 de la misma quedan protegidos por dicha acción. En términos generales son los siguientes: </p>
                        <ul>
                            <li>Derecho a la vida.</li>
                            <li>Igualdad ante la ley.</li>
                            <li>Derecho ser juzgado por los tribunales que establece la ley y que estén establecidos antes de la ejecución del hecho.</li>
                            <li>Derecho a la protección de la vida privada y a la honra de la persona y su familia. </li>
                            <li>Inviolabilidad del hogar y de toda forma de comunicación privada. </li>
                            <li>Libertad de conciencia y libertad de culto. </li>
                            <li>Derecho a elegir el sistema de salud, sea estatal o privado. </li>
                            <li>Libertad de enseñanza. </li>
                            <li>Libertad de opinión e información. </li>
                            <li>Derecho de reunión. </li>
                            <li>Derecho de asociación. </li>
                            <li>Libertad de trabajo, su libre elección y libre contratación. </li>
                            <li>Derecho de sindicalización. </li>
                            <li>Libertad para desarrollar cualquier actividad económica. </li>
                            <li>Derecho a no ser discriminado en el trato del Estado en materia económica.</li>
                            <li>Libertad para adquirir el dominio de toda clase de bienes. </li>
                            <li>Derecho de propiedad. </li>
                            <li>Derecho de propiedad intelectual e industrial. </li>
                            <li>Derecho a vivir en un medio ambiente libre de contaminación, cuando sea afectado por un acto u omisión imputable a una persona o autoridad determinada.</li>
                        </ul>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-4"> ¿Cuál es el objetivo del recurso de protección?  </a>
                </div>
                <div id="panel-element-4" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Que la Corte de Apelaciones respectiva ordene todas las medidas necesarias para reestablecer el derecho vulnerado y asegurar su protección. Igual se pueden presentar otras acciones ante la autoridad o los Tribunales de Justicia.</p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-5">¿Quiénes pueden interponer un recurso de protección?</a>
                </div>
                <div id="panel-element-5" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Cualquier persona, natural o jurídica, o un grupo de personas, que haya sufrido la perturbación o amenaza de estos derechos, ya sea directamente o un tercero en representación de ellas. No siendo necesario contar con abogado para la presentación del recurso.</p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-6">¿Dónde se debe interponer el recurso de protección y qué requisitos debe cumplir?  </a>
                </div>
                <div id="panel-element-6" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Ante la Corte de Apelaciones en cuya jurisdicción se cometió el acto o se incurrió en la omisión arbitraria o ilegal que ocasione la vulneración. La lista completa la puede encontrar 
                            <a href="http://pjud.cl/PDF/InfoInstitucional/tablacortedeapelaciones.pdf" target="_blank">aquí.</a></p>
                        <p>El recurso se debe presentar por escrito y fundamentar cuál es la acción u omisión ilegal o arbitraria que sirve de base a su interposición así como los derechos o garantías consagradas en la Constitución que se ven vulneradas.</p>
                        
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-7"> ¿Cuál es el plazo para presentar un recurso de protección?</a>
                </div>
                <div id="panel-element-7" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>El plazo es de 30 días corridos contados desde que ocurre el acto o amenaza que motiva el recurso o desde que se tuvo conocimiento de dicho acto o amenaza, hecho que debe ser acreditado en la presentación realizada ante la Corte.</p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-8"> ¿Cuáles son los efectos que produce el que se acoja el recurso?</a>
                </div>
                <div id="panel-element-8" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>El hecho de que se acoja un recurso de protección implica que la Corte determinará las acciones que deben llevarse a cabo para el restablecimiento del derecho infringido y la protección del afectado.</p>
                    </div>
                </div>
            </div>
            <div class="panel panel-default">
                <div class="panel-heading">
                    <a class="panel-title" data-toggle="collapse" data-parent="#panel-questions" href="#panel-element-9">8. ¿Es posible la interposición posterior de otras acciones?</a>
                </div>
                <div id="panel-element-9" class="panel-collapse collapse">
                    <div class="panel-body">
                        <p>Así es, ya que el rechazo o la acogida del recurso, no obsta a que el afectado pueda interponer con posterioridad, ante la autoridad o tribunal correspondiente, las acciones que estime convenientes.</p>
                    </div>
                </div>
